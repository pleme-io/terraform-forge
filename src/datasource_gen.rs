    #[test]
    fn generate_datasource_attributes_test() {
        let (ds, api, defaults) = make_ds_test_data();
        let attrs = generate_datasource_attributes(&ds, &api, &defaults).expect("gen");

        // token should be skipped
        assert!(attrs.iter().all(|a| a.tf_name != "token"));

        // name should be Optional (data sources don't have Required)
        let name_attr = attrs.iter().find(|a| a.tf_name == "name").expect("name");
        assert!(!name_attr.required);
        assert!(name_attr.optional);

        // access_id should exist
        let access = attrs
            .iter()
            .find(|a| a.tf_name == "access_id")
            .expect("access_id");
        assert!(access.computed);
    }

